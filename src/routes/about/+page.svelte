		and Ethiopia, through Northern Uganda and eastern Congo (DRC), into western Kenya, and the Mara Region
		of Tanzania. Their Luo languages belong to the western branch of the Nilotic language family. The
		Luo groups in South Sudan include the Shilluk, Anuak, Pari, Acholi, Balanda Boor, Thuri and Luwo.
		Those in Uganda include the Alur, Acholi, Jonam and Padhola. The ones in Kenya and Tanzania are the
		Joluo (also called Luo in Kenyan English). The Joluo and their language Dholuo are also known as
		the "Luo proper" by Kenya based observers, even though their dialect has more Bantu loan words than
		the rest. The level of historical separation between these groups is estimated at about eight centuries..
